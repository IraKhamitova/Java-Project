package ifElse;

public class IntroToIFElse {
    public static void main(System[]args){
        boolean b1=4<2;
        if (b1=4<2){
            System.out.println(4<2);
        } else{
            System.out.println(4>2);

        }

        boolean b2=4+4==8;
        if (b2=4+4==8){
            System.out.println(4+4==8);}
        else {
            System.out.println(4-4==8);
        }




    }
}
