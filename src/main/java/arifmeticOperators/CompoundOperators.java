package arifmeticOperators;

public class CompoundOperators {
    public static void main(String[]args){
        int a =5;
        a++;//a=6
        a++;
        a++;
        System.out.println(a);//8
        a--;//a=a-1

        int age=18;
        System.out.println(age++);//post increment 18
        System.out.println(age);//19
        

    }
}
